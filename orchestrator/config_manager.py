def save_settings(data: Dict[str, Any]) -> None:
    SETTINGS_PATH.parent.mkdir(parents=True, exist_ok=True)
    SETTINGS_PATH.write_text(json.dumps(data, indent=2, ensure_ascii=True) + "\n", encoding="utf-8")

def env_line(key: str, value: str) -> str:
    import re
    if re.fullmatch(r"[A-Za-z0-9_./:@%+=,\-]+", value):
        return f"{key}={value}"
    return f"{key}={json.dumps(value)}"

def write_runtime_env(data: Dict[str, str]) -> None:
    def write_env_file(path: Path, values: Dict[str, str]) -> None:
        lines = [
            "# Generated by the dashboard MCP secrets vault.",
            "# This file is gitignored. Restart docker compose services to apply changes.",
            *[env_line(key, value) for key, value in sorted(values.items())],
        ]
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text("\n".join(lines) + "\n", encoding="utf-8")

    SECRETS_ENV_PATH.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Generated by the dashboard MCP secrets vault.",
        "# This file is gitignored. Restart docker compose services to apply changes.",
        *[env_line(key, value) for key, value in sorted(data.items())],
    ]
    SECRETS_ENV_PATH.write_text("\n".join(lines) + "\n", encoding="utf-8")

    catalog = load_mcp_catalog()
    for server_name, server in catalog.get("servers", {}).items():
        keys = server.get("env_keys", []) or []
        server_values = {key: data[key] for key in keys if key in data}
        write_env_file(MCP_ENV_DIR / f"{server_name}.env", server_values)

def save_secret_store(data: Dict[str, str]) -> None:
    SECRETS_PATH.parent.mkdir(parents=True, exist_ok=True)
    encrypted_data = {}
    for key, val in data.items():
        if val:
            encrypted_data[key] = encrypt_secret(val)
    SECRETS_PATH.write_text(json_dumps(encrypted_data), encoding="utf-8")
    write_runtime_env(data)
    try:
        SECRETS_PATH.chmod(0o600)
        SECRETS_ENV_PATH.chmod(0o600)
        for path in MCP_ENV_DIR.glob("*.env"):
            path.chmod(0o600)
    except Exception:
        pass

def load_secret_store() -> Dict[str, str]:
    if not SECRETS_PATH.exists():
        return {}
    try:
        data = json_loads(SECRETS_PATH.read_text(encoding="utf-8"))
        result = {}
        needs_migration = False
        for key, value in data.items():
            if not isinstance(key, str):
                continue
            if isinstance(value, str):
                result[key] = value
                if value:
                    needs_migration = True
            else:
                result[key] = decrypt_secret(value)
                
        if needs_migration:
            print("Migrating local secrets store to secure encrypted format...")
            save_secret_store(result)
            
        return result
    except Exception as exc:
        print(f"Error loading secrets from store: {exc}")
        return {}

