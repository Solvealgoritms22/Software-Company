def mask_secret(value: str) -> str:
    if not value:
        return ""
    if len(value) <= 8:
        return "•" * len(value)
    return f"{value[:3]}{'•' * 8}{value[-4:]}"

def secret_status(key: str, store: Optional[Dict[str, str]] = None) -> Dict[str, Any]:
    store = store if store is not None else load_secret_store()
    local_value = store.get(key)
    env_value = os.getenv(key)
    value = local_value or env_value or ""
    return {
        "key": key,
        "configured": bool(value),
        "masked": mask_secret(value),
        "source": "local_store" if local_value else ("runtime_env" if env_value else "missing"),
    }

def export_mcp_config(client: str, catalog: Dict[str, Any]) -> Dict[str, Any]:
    servers = {
        name: server
        for name, server in catalog.get("servers", {}).items()
        if server.get("enabled") and server.get("kind") == "stdio" and server.get("command")
    }
    if client == "codex":
        blocks = []
        for name, server in servers.items():
            args = ", ".join(f'"{arg}"' for arg in server.get("args", []))
            blocks.append(f'[mcp_servers.{name}]\ncommand = "{server["command"]}"\nargs = [{args}]')
        return {"config_toml": "\n\n".join(blocks)}
    if client == "vscode":
        return {
            "servers": {
                name: {
                    "command": server["command"],
                    "args": server.get("args", []),
                    **({"env": server.get("env")} if server.get("env") else {}),
                }
                for name, server in servers.items()
            }
        }
    return {
        "mcpServers": {
            name: {
                "command": server["command"],
                "args": server.get("args", []),
                **({"env": server.get("env")} if server.get("env") else {}),
            }
            for name, server in servers.items()
        }
    }

